package taxonomy;

/**
 * Defines the hierarchical rank of taxa.  The lowest level has the mathematically smallest associated value
 * of the ordering.
 * @author Alex
 *
 */
public enum TaxonRank {
	VARIETY,
	SUBSPECIES,
	SPECIES,
	SUBSECTION,
	SECTION,
	SUBGENUS,
	GENUS,
	SUBTRIBE,
	TRIBE,
	SUBFAMILY,
	FAMILY,
	ORDER,
	CLASS,
	PHYLLUM,
	KINGDOM	
}
